export function getTranslation(lang, key) {
  const langTranslations = translations[lang] || translations[defaultLanguage];
  return langTranslations[key] || translations[defaultLanguage][key] || key;
}

export function getCurrentLanguage(req) {
  const acceptLanguage = req?.headers?.get('accept-language') || '';
  const preferredLanguage = acceptLanguage.split(',')[0].split('-')[0];

  if (languages[preferredLanguage]) {
    return preferredLanguage;
  }

  return defaultLanguage;
}